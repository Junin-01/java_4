
public class Conta {
			
	private int senha = 122;
	private int endereço;
	private int agência;
   private int saldo;
   
   
public int getSenha() {
	return senha;
}
public void setSenha(int senha) {
	this.senha = senha;
}
public int getEndereço() {
	return endereço;
}
public void setEndereço(int endereço) {
	this.endereço = endereço;
}
public int getAgência() {
	return agência;
}
public void setAgência(int agência) {
	this.agência = agência;
}
public int getSaldo() {
	return saldo;
}
public void setSaldo(int saldo) {
	this.saldo = saldo;
}

   
}